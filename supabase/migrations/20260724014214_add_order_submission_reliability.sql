create or replace function public.record_xendit_invoice(
  p_submission_id uuid,
  p_pending_order_id uuid,
  p_attempt_id uuid,
  p_invoice_id text,
  p_external_id text,
  p_status text,
  p_amount numeric,
  p_payment_url text,
  p_expiry_date timestamptz,
  p_user_id uuid
)
returns public.xendit_payments
language plpgsql
security invoker
set search_path = ''
as $$
declare
  v_payment public.xendit_payments;
  v_pending public.pending_facebook_orders;
begin
  if p_submission_id is null
    or p_pending_order_id is null
    or nullif(p_invoice_id, '') is null
    or nullif(p_external_id, '') is null
    or p_amount is null
    or p_amount <= 0
  then
    raise exception using
      errcode = '22023',
      message = 'payment_record_invalid';
  end if;

  perform pg_catalog.pg_advisory_xact_lock(
    pg_catalog.hashtextextended(p_submission_id::text, 0)
  );

  select *
  into v_pending
  from public.pending_facebook_orders
  where id = p_pending_order_id
    and submission_id = p_submission_id
  for update;

  if not found then
    raise exception using
      errcode = 'P0002',
      message = 'pending_order_not_found';
  end if;

  select *
  into v_payment
  from public.xendit_payments
  where submission_id = p_submission_id
  for update;

  if found then
    if v_payment.xendit_invoice_id <> p_invoice_id
      or v_payment.xendit_external_id <> p_external_id
      or v_payment.amount <> p_amount
    then
      raise exception using
        errcode = '22023',
        message = 'payment_invoice_conflict';
    end if;

    return v_payment;
  end if;

  insert into public.xendit_payments (
    order_id,
    xendit_invoice_id,
    status,
    amount,
    payment_link_url,
    expiry_date,
    xendit_external_id,
    user_id,
    submission_id,
    attempt_id
  )
  values (
    p_pending_order_id::text,
    p_invoice_id,
    p_status,
    p_amount,
    p_payment_url,
    p_expiry_date,
    p_external_id,
    coalesce(p_user_id, '00000000-0000-0000-0000-000000000000'::uuid),
    p_submission_id,
    p_attempt_id
  )
  returning *
  into v_payment;

  update public.pending_facebook_orders
  set
    submission_status = 'payment_pending',
    updated_at = now()
  where id = p_pending_order_id;

  return v_payment;
end;
$$;

