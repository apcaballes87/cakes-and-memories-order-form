create or replace function public.finalize_xendit_order(
  p_submission_id uuid,
  p_invoice_id text,
  p_external_id text,
  p_paid_amount numeric,
  p_paid_at timestamptz,
  p_payment_method text default null
)
returns table (
  order_id bigint,
  order_number text,
  created boolean
)
language plpgsql
security invoker
set search_path = ''
as $$
declare
  v_payment public.xendit_payments;
  v_pending public.pending_facebook_orders;
  v_order_data jsonb;
  v_created record;
begin
  if p_submission_id is null
    or nullif(p_invoice_id, '') is null
    or nullif(p_external_id, '') is null
    or p_paid_amount is null
    or p_paid_amount <= 0
  then
    raise exception using
      errcode = '22023',
      message = 'paid_invoice_invalid';
  end if;

  perform pg_catalog.pg_advisory_xact_lock(
    pg_catalog.hashtextextended(p_submission_id::text, 0)
  );

  select *
  into v_payment
  from public.xendit_payments
  where submission_id = p_submission_id
  for update;

  if not found then
    raise exception using
      errcode = 'P0002',
      message = 'payment_record_not_found';
  end if;

  if v_payment.xendit_invoice_id <> p_invoice_id
    or v_payment.xendit_external_id <> p_external_id
    or v_payment.amount <> p_paid_amount
  then
    raise exception using
      errcode = '22023',
      message = 'paid_invoice_conflict';
  end if;

  select *
  into v_pending
  from public.pending_facebook_orders
  where id::text = v_payment.order_id
    and submission_id = p_submission_id
  for update;

  if not found then
    raise exception using
      errcode = 'P0002',
      message = 'pending_order_not_found';
  end if;

  v_order_data := v_pending.order_data
    - 'id'
    - 'created_at'
    - 'submission_id'
    - 'order_number_text'
    - 'totalorderprice';

  v_order_data := v_order_data || jsonb_build_object(
    'paymentOption',
    'XENDIT',
    'payment',
    p_paid_amount
  );

  if coalesce(v_order_data->>'facebookname', '') not like '%[PAID VIA XENDIT]%' then
    v_order_data := jsonb_set(
      v_order_data,
      '{facebookname}',
      to_jsonb(
        trim(
          coalesce(v_order_data->>'facebookname', '')
          || ' [PAID VIA XENDIT]'
        )
      ),
      true
    );
  end if;

  select *
  into v_created
  from public.create_order_from_submission(
    p_submission_id,
    v_order_data,
    v_pending.preorder_facebook_u
  );

  update public.xendit_payments
  set
    status = 'PAID',
    paid_amount = p_paid_amount,
    paid_at = coalesce(p_paid_at, now()),
    payment_method = coalesce(p_payment_method, payment_method),
    final_order_id = v_created.order_id,
    updated_at = now()
  where id = v_payment.id;

  update public.pending_facebook_orders
  set
    submission_status = 'completed',
    updated_at = now()
  where id = v_pending.id;

  update public."New PRE Facebook Orders"
  set
    submitted = true,
    submission_status = 'completed',
    submission_updated_at = now()
  where submission_id = p_submission_id
     or (
       v_pending.preorder_facebook_u is not null
       and "facebookU" = v_pending.preorder_facebook_u
     );

  return query
  select
    v_created.order_id,
    v_created.order_number,
    v_created.created;
end;
$$;

