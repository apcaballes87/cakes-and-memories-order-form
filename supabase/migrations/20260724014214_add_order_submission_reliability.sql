create or replace function public.record_xendit_payment_status(
  p_submission_id uuid,
  p_invoice_id text,
  p_status text,
  p_payment_method text default null,
  p_paid_amount numeric default null,
  p_paid_at timestamptz default null
)
returns public.xendit_payments
language plpgsql
security invoker
set search_path = ''
as $$
declare
  v_payment public.xendit_payments;
  v_pending_state text;
begin
  if p_submission_id is null or nullif(p_invoice_id, '') is null then
    raise exception using
      errcode = '22023',
      message = 'payment_status_reference_invalid';
  end if;

  if p_status is null
    or p_status not in ('PENDING', 'PAID', 'SETTLED', 'EXPIRED', 'FAILED')
  then
    raise exception using
      errcode = '22023',
      message = 'payment_status_invalid';
  end if;

  if p_status in ('PAID', 'SETTLED') then
    raise exception using
      errcode = '22023',
      message = 'paid_status_requires_finalization';
  end if;

  perform pg_catalog.pg_advisory_xact_lock(
    pg_catalog.hashtextextended(p_submission_id::text, 0)
  );

  update public.xendit_payments
  set
    status = p_status,
    payment_method = coalesce(p_payment_method, payment_method),
    paid_amount = coalesce(p_paid_amount, paid_amount),
    paid_at = coalesce(p_paid_at, paid_at),
    updated_at = now()
  where submission_id = p_submission_id
    and xendit_invoice_id = p_invoice_id
  returning *
  into v_payment;

  if not found then
    raise exception using
      errcode = 'P0002',
      message = 'payment_record_not_found';
  end if;

  v_pending_state := case
    when p_status in ('EXPIRED', 'FAILED') then 'payment_failed'
    else 'payment_pending'
  end;

  update public.pending_facebook_orders
  set
    submission_status = v_pending_state,
    updated_at = now()
  where submission_id = p_submission_id;

  update public."New PRE Facebook Orders"
  set
    submitted = false,
    submission_status = v_pending_state,
    submission_updated_at = now()
  where submission_id = p_submission_id;

  return v_payment;
end;
$$;

