create or replace function public.prepare_xendit_submission(
  p_submission_id uuid,
  p_order_data jsonb,
  p_request_fingerprint text,
  p_preorder_facebook_u uuid,
  p_route_kind text
)
returns table (
  pending_order_id uuid,
  final_order_id bigint,
  final_order_number text,
  created boolean
)
language plpgsql
security invoker
set search_path = ''
as $$
declare
  v_pending public.pending_facebook_orders;
  v_final public."New Facebook Orders";
  v_pre_submitted boolean;
  v_pre_submission_id uuid;
begin
  if p_submission_id is null then
    raise exception using
      errcode = '22023',
      message = 'submission_id_required';
  end if;

  if p_order_data is null or jsonb_typeof(p_order_data) <> 'object' then
    raise exception using
      errcode = '22023',
      message = 'order_data_invalid';
  end if;

  if nullif(p_request_fingerprint, '') is null then
    raise exception using
      errcode = '22023',
      message = 'request_fingerprint_required';
  end if;

  if p_route_kind is null
    or p_route_kind not in ('default', 'facebook_uuid', 'subscriber_psid')
  then
    raise exception using
      errcode = '22023',
      message = 'route_kind_invalid';
  end if;

  perform pg_catalog.pg_advisory_xact_lock(
    pg_catalog.hashtextextended(p_submission_id::text, 0)
  );

  select *
  into v_final
  from public."New Facebook Orders"
  where submission_id = p_submission_id
  limit 1;

  if found then
    return query
    select
      null::uuid,
      v_final.id,
      v_final.order_number_text,
      false;
    return;
  end if;

  if p_preorder_facebook_u is not null then
    select submitted, submission_id
    into v_pre_submitted, v_pre_submission_id
    from public."New PRE Facebook Orders"
    where "facebookU" = p_preorder_facebook_u
    for update;

    if found
      and v_pre_submitted
      and v_pre_submission_id is distinct from p_submission_id
    then
      raise exception using
        errcode = '23505',
        message = 'preorder_already_submitted';
    end if;
  end if;

  select *
  into v_pending
  from public.pending_facebook_orders
  where submission_id = p_submission_id
  for update;

  if found then
    if v_pending.request_fingerprint is distinct from p_request_fingerprint then
      raise exception using
        errcode = '22023',
        message = 'submission_payload_conflict';
    end if;

    return query
    select
      v_pending.id,
      null::bigint,
      null::text,
      false;
    return;
  end if;

  insert into public.pending_facebook_orders (
    order_data,
    submission_id,
    submission_status,
    request_fingerprint,
    preorder_facebook_u,
    route_kind,
    updated_at
  )
  values (
    p_order_data,
    p_submission_id,
    'payment_pending',
    p_request_fingerprint,
    p_preorder_facebook_u,
    p_route_kind,
    now()
  )
  returning *
  into v_pending;

  update public."New PRE Facebook Orders"
  set
    submission_id = p_submission_id,
    submitted = false,
    submission_status = 'payment_pending',
    submission_updated_at = now()
  where p_preorder_facebook_u is not null
    and "facebookU" = p_preorder_facebook_u
    and (
      submission_id is null
      or submission_id = p_submission_id
    );

  return query
  select
    v_pending.id,
    null::bigint,
    null::text,
    true;
end;
$$;

