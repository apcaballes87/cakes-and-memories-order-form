-- Phase 2 order-submission reliability primitives.
--
-- This migration is intentionally additive. It does not enable RLS or revoke
-- the legacy anonymous grants on the two Facebook order tables. Those changes
-- must happen only after every form and admin consumer has moved to the
-- server-owned endpoints and the cutover audit has passed.

alter table public."New Facebook Orders"
  add column if not exists submission_id uuid;

alter table public."New PRE Facebook Orders"
  add column if not exists submission_id uuid,
  add column if not exists submission_status text not null default 'draft',
  add column if not exists submission_updated_at timestamptz not null default now();

alter table public.pending_facebook_orders
  add column if not exists submission_id uuid,
  add column if not exists submission_status text not null default 'draft',
  add column if not exists request_fingerprint text,
  add column if not exists preorder_facebook_u uuid,
  add column if not exists route_kind text,
  add column if not exists updated_at timestamptz not null default now();

alter table public.xendit_payments
  add column if not exists submission_id uuid,
  add column if not exists final_order_id bigint,
  add column if not exists attempt_id uuid;

update public."New PRE Facebook Orders"
set submission_status = case when submitted then 'completed' else 'draft' end
where submission_status is distinct from
  case when submitted then 'completed' else 'draft' end;

do $$
begin
  if not exists (
    select 1
    from pg_constraint
    where conrelid = 'public."New PRE Facebook Orders"'::regclass
      and conname = 'new_pre_facebook_orders_submission_status_check'
  ) then
    alter table public."New PRE Facebook Orders"
      add constraint new_pre_facebook_orders_submission_status_check
      check (submission_status in (
        'draft',
        'payment_pending',
        'completed',
        'payment_failed'
      ));
  end if;

  if not exists (
    select 1
    from pg_constraint
    where conrelid = 'public.pending_facebook_orders'::regclass
      and conname = 'pending_facebook_orders_submission_status_check'
  ) then
    alter table public.pending_facebook_orders
      add constraint pending_facebook_orders_submission_status_check
      check (submission_status in (
        'draft',
        'payment_pending',
        'completed',
        'payment_failed'
      ));
  end if;

  if not exists (
    select 1
    from pg_constraint
    where conrelid = 'public.pending_facebook_orders'::regclass
      and conname = 'pending_facebook_orders_route_kind_check'
  ) then
    alter table public.pending_facebook_orders
      add constraint pending_facebook_orders_route_kind_check
      check (
        route_kind is null
        or route_kind in ('default', 'facebook_uuid', 'subscriber_psid')
      );
  end if;

  if not exists (
    select 1
    from pg_constraint
    where conrelid = 'public.xendit_payments'::regclass
      and conname = 'xendit_payments_final_order_id_fkey'
  ) then
    alter table public.xendit_payments
      add constraint xendit_payments_final_order_id_fkey
      foreign key (final_order_id)
      references public."New Facebook Orders" (id)
      on delete set null;
  end if;
end
$$;

